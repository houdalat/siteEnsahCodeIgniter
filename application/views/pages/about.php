                                <h2 class="event-title">Génie énergétique et énergies renouvelables</h2> 
                                <p>
								


 
             </p><h5> Objectifs de la formation </h5>
 	         <p>
 	         De nos jours, pour pouvoir satisfaire les besoins croissants en énergie de la population, plus de 60% de la production mondiale d’énergie provient des combustibles fossiles, environ 20% du nucléaire et le reste des sources d’énergie renouvelables. Toutefois, l’utilisation des ressources fossiles dans de telles proportions pose, d’une part, des problèmes d’environnement, notamment par l’émission de CO2 (gaz à effet de serre) et de gaz polluants (SO2, NOx, CO, CH4, chlorofluorocarbones, particules solides, etc.) et, d’autre part, entraînent l’augmentation des prix et l’épuisement de ces ressources.<br>
 	           Les problèmes annoncés posent des questions essentielles pour le développement durable de la planète. Les premières décisions politiques au niveau mondial ont été prises au cours du sommet de Kyoto en 1997. Le but visé est de prendre des mesures de précaution pour prévoir, prévenir ou atténuer les causes des changements climatiques. En parallèle, la recherche de nouvelles technologies de production d’énergie dites propres et efficaces, a été encouragée et entreprise dans plusieurs pays.<br>
 	           Pour répondre aux besoins immédiats et futurs de nos sociétés, et réagir avec efficacité et rigueur aux défis de ces mutations, nous devons former une nouvelle race d’ingénieur capable de produire, d’utiliser rationnellement l’énergie et gérer un environnement énergétique sain et vraiment durable.<br>
 	           L’objectif principal de cette filière est de former des ingénieurs en Génie énergétique et énergies renouvelables possédant une formation polyvalente, adaptée, aux évolutions technologiques du monde de demain, et qui peuvent intervenir dans différents secteurs de l’énergie tout au long du processus de production et de distribution.<br>
 	           Les enseignements proposés allient connaissances théoriques et techniques ainsi que des réalisations concrètes, à travers des projets de fin d’étude et des stages au sein de l’industrie. Cette stratégie adoptée, permettra aux lauréats de s’adapter, répondre et réagir avec efficacité et rigueur aux défis des mutations et évolutions extrêmement rapides auxquels les entreprises sont confrontées, aux nouveaux modes de management, aux pressions sans cesse croissantes de la concurrence, de la technologie et du client, ainsi qu’à la protection de l’environnement et des risques majeurs.<br>
 </p>
    
		    <h5> Compétences à acquérir </h5> 
             <p>Les ingénieurs diplômés en génie  énergétique et énergies renouvelables disposent d'une double compétence  technique et managériale, ils seront capables de :</p> 
